package cn.lic.model;

import java.util.Date;

public class Fensi {
	private int id;
	private	int fensi_id;
	private Date time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFensi_id() {
		return fensi_id;
	}
	public void setFensi_id(int fensi_id) {
		this.fensi_id = fensi_id;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	
}
